/**
 * Build the llms.txt tool-list block from the SAME `tools.json` the reference
 * pages render (04_LAUNCH_SPEC §1.5 step 3, §1.6). The prebuild step writes
 * `tools.json` before this config is evaluated, so an IDE agent fetching
 * `/llms.txt` learns the tools without scraping every page. Framed honestly:
 * llms.txt is cheap and well-targeted at the IDE-agent audience, not a traffic
 * engine.
 */
function buildToolBlock() {
  /** @type {{ name: string; description?: string; annotations?: { readOnlyHint?: boolean } }[]} */
  let tools = [];
  try {
    tools = JSON.parse(readFileSync(new URL('./src/data/tools.json', import.meta.url), 'utf8'));
  } catch {
    return '';
  }
  const lines = tools.map((tool) => {
    const kind = tool.annotations?.readOnlyHint ? 'read' : 'write';
    // First sentence only, but shield the "e.g." / "i.e." abbreviations (which
    // contain a period+space) so they do not truncate the line mid-phrase
    // (e.g. live_insert_device's "(e.g. 'Reverb')").
    const oneLine = (tool.description ?? '')
      .replace(/\b(e\.g|i\.e)\./g, '$1<dot>')
      .split('. ')[0]
      .replaceAll('<dot>', '.')
      .replace(/\.$/, '');
    return `- \`${tool.name}\` (${kind}): ${oneLine}.`;
  });
  return [
    'Loophole Bridge MCP tools (generated from the running server, the source of truth):',
    '',
    ...lines,
  ].join('\n');
}

